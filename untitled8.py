import requests

# Récupérer les 10 premières cryptomonnaies par capitalisation de marché
url = 'https://api.coingecko.com/api/v3/coins/markets'
params = {
    'vs_currency': 'usd',
    'order': 'market_cap_desc',
    'per_page': 10,  # Nombre de cryptomonnaies
    'page': 1
}

response = requests.get(url, params=params)
data = response.json()

# Afficher les 10 meilleures cryptos
for coin in data:
    print(f"{coin['name']} ({coin['symbol'].upper()}): ${coin['current_price']}")

