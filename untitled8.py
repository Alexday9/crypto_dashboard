import streamlit as st
import requests

# Récupération des données via l'API CoinGecko
url = "https://api.coingecko.com/api/v3/coins/markets"
params = {
    "vs_currency": "usd",
    "order": "market_cap_desc",
    "per_page": 10,
    "page": 1
}

response = requests.get(url, params=params)
data = response.json()

# Titre du Dashboard
st.title("Top 10 Cryptomonnaies")

# Tableau avec les données des cryptomonnaies
st.write("Voici la liste des 10 meilleures cryptomonnaies par capitalisation boursière :")

# Créer un tableau avec les informations
table_data = []
for coin in data:
    table_data.append([coin['name'], coin['current_price'], coin['market_cap'], coin['symbol']])

# Affichage sous forme de tableau
st.write("Les 10 meilleures cryptomonnaies")
st.table(table_data)